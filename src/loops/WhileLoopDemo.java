package loops;

public class WhileLoopDemo {

	public static void main(String[] args) {

		int count = 2; //initialized (starting point)
		
		//ending point at our condition
		
		while (count < 11) {      
		    System.out.println("The count is " + count);
		    count+=2; // remember, this increases the value of count by 1
		    
		    //count +=2 (shows odds if int count = 1, or evens if int count =2
		} 
			
		
	}

}
